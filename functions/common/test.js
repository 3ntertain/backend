function calculateDaysBetweenDate() {
    var date1 = new Date("2018-01-01");
    var date2 = new Date("2018-01-02");
    var diff = date2.getTime() - date1.getTime();
    var days = diff / (1000 * 60 * 60 * 24);
    return days;
}



